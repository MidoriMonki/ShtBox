map::map(){
    dirt = textureManager::loadTexture("assets/germ.png");
    grass = textureManager::loadTexture("assets/player.png");
    water = textureManager::loadTexture("assets/player.png");

    loadMap(lvl1);
    srcRect.x = srcRect.y = 0;
    srcRect.w = destRect.w = 32;
    srcRect.h = destRect.h = 32;

    destRect.x = destRect.y = 0;
}

void map::loadMap(int arr[20][25]){
    for (int row=0;row<20;row++){
        for(int col=0;col<25;col++){
            mapArray[row][col] = arr[row][col];
        }
    }
}

void map::drawMap(){

    int type = 0;

    for (int row=0;row<20;row++){
        for(int col=0;col<25;col++){
            type = mapArray[row][col];

            destRect.x = col*32;
            destRect.y = row*32;
            switch(type){
                case 0: 
                    textureManager::draw(dirt, srcRect, destRect);
                    break;
                case 1: 
                    textureManager::draw(grass, srcRect, destRect);
                    break;
                case 2: 
                    textureManager::draw(water, srcRect, destRect);
                    break;
                default:
                    break;
            }
        }
    }
}